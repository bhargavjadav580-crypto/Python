mytuple = ('A','B','C','D','E','F')
print(mytuple)

# get
print(mytuple[4])
print(mytuple[-2])

print(mytuple[1:4])
print(mytuple[3:5])
print(mytuple[1:]) 
print(mytuple[:4])
print(mytuple[:])

print(mytuple[-5:-2])
print(mytuple[-4:-2])
print(mytuple[-5:])
print(mytuple[:-3])
print(mytuple[:])


